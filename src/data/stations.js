// Search stations by name or area
export function searchStations(query) {
  if (!query || typeof query !== 'string') {
    return []
  }
  
  const searchTerm = query.toLowerCase().trim()
  
  return stations.filter(station => 
    station.name.toLowerCase().includes(searchTerm) ||
    (station.area && station.area.toLowerCase().includes(searchTerm)) ||
    (station.description && station.description.toLowerCase().includes(searchTerm))
  )
}

// Get stations by area
export function getStationsByArea(area) {
  if (!area || typeof area !== 'string') {
    return []
  }
  
  return stations.filter(station => 
    station.area && station.area.toLowerCase() === area.toLowerCase()
  )
}

// Get stations by type
export function getStationsByType(type) {
  return stations.filter(station => station.type === type)
}

// Get station by ID
export function getStationById(id) {
  return stations.find(station => station.id === id) || null
}
